class Solution:
    
    def countGoodStrings(self, low: int, high: int, zero: int, one: int) -> int:
        MOD = 10**9 +7
        dp = [0] *(high+1)
        dp[0] = 1

        for i in range(1, high+1):
            if i>=0:
                dp[i] = (dp[i]+dp[i-zero]) % MOD
            if i >=one:
                dp[i] = (dp[i]+dp[i-one]) %MOD
        result = sum(dp[low:high+1])%MOD
        return result